package IfStatements;

public class CurlyBracketsPractice {
    public static void main(String[]args){
        int  appleNumber=20;
       int  orangeNumber=30;

        if (appleNumber>orangeNumber){
            appleNumber++;

            System.out.println("there are more oranges in the store than apples"+appleNumber);
        }



        if (orangeNumber>appleNumber){
            orangeNumber++;
            ++orangeNumber;
            System.out.println("there are more oranges in the store than apples"+orangeNumber);
        }








    }
}
