package primitives;

public class charPractice {
        public static void main(String[]args){
                char firstLetter = 'A'; // value for a char data type needs to be in the single quotation mark (' A ')
                System.out.println(firstLetter);

                char secondLetter = '6';
                System.out.println(secondLetter);

                char thirdLetter = '^';
                System.out.println(thirdLetter);

                char forthLetter = 66;
                System.out.println(forthLetter);  // it give B which comes from Ascii table
                // char forthLetter1 = '66'; //you can store only single character in single quotation mark for char

                char fifthLetter = 2550;
                System.out.println(fifthLetter);

                char sixthLetter = 42;  // if you assign a number for a char data type, it will print a symbol/character from ASCII table
                System.out.println(sixthLetter);

                double d = '?';
                System.out.println(d);  // if you assign a symbol/character from ASCII table, it will print the number/value for it.

                int c = '?';
                System.out.println(c);

                // TASK : create a int data type and assign a character  for it and print out
                int b = '%';
                System.out.println(b);

                int x = 'A';
                System.out.println(x);

                int y = '2';
                System.out.println(y);

                int t = 'T', k= 'K', g='G', n='N', m='M';
                System.out.println("" + "----" + t + "----" +k + "----" + g + "----" + n +"----" + m);
                System.out.println(t + k +g + n + m);
        }
}



